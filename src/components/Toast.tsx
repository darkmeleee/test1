"use client";

import React, { useState, useEffect } from "react";

interface Toast {
  id: string;
  message: string;
  type: "error" | "success" | "warning";
  duration?: number;
}

interface ToastContextType {
  showToast: (message: string, type?: "error" | "success" | "warning", duration?: number) => void;
}

export const ToastContext = React.createContext<ToastContextType | undefined>(undefined);

export function useToast() {
  const context = React.useContext(ToastContext);
  if (!context) {
    throw new Error("useToast must be used within a ToastProvider");
  }
  return context;
}

export function ToastProvider({ children }: { children: React.ReactNode }) {
  const [toasts, setToasts] = useState<Toast[]>([]);

  const showToast = (message: string, type: "error" | "success" | "warning" = "error", duration = 3000) => {
    const id = Date.now().toString();
    const newToast: Toast = { id, message, type, duration };
    
    setToasts(prev => [...prev, newToast]);
    
    if (duration > 0) {
      setTimeout(() => {
        setToasts(prev => prev.filter(toast => toast.id !== id));
      }, duration);
    }
  };

  const removeToast = (id: string) => {
    setToasts(prev => prev.filter(toast => toast.id !== id));
  };

  return (
    <ToastContext.Provider value={{ showToast }}>
      {children}
      <div className="fixed top-4 right-4 z-50 space-y-2">
        {toasts.map((toast) => (
          <div
            key={toast.id}
            className={`
              px-4 py-3 rounded-lg shadow-lg text-white max-w-sm animate-pulse
              ${toast.type === "error" ? "bg-red-600" : ""}
              ${toast.type === "success" ? "bg-green-600" : ""}
              ${toast.type === "warning" ? "bg-yellow-600" : ""}
            `}
          >
            <div className="flex items-center justify-between">
              <span className="text-sm font-medium">{toast.message}</span>
              <button
                onClick={() => removeToast(toast.id)}
                className="ml-4 text-white hover:text-gray-200"
              >
                Ã—
              </button>
            </div>
          </div>
        ))}
      </div>
    </ToastContext.Provider>
  );
}
